def check_user():
    # Запрашиваем у пользователя имя
    name = input("Введите ваше имя: ")

    # Проверяем, является ли пользователь Валерой
    if name.lower() == "валера":
        print("Вы Валера!")
    else:
        print("Вы не Валера.")


# Вызов функции для проверки пользователя
check_user()
